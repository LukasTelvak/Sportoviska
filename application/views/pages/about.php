<h3>O aplikácii</h3>
<br>
Mojou úlohou bolo vytvoriť aplikáciu pre športový komplex ktorý má svoje športoviská a tie prenajíma svojim zákazníkom.
V databáze sa nachádza 5 tabuliek. V prvej tabuľke sa evidujú zákazníci a základné údaje o nich ako telefónne číslo alebo
mesto v ktorom žijú. V ďaľších tabuľkách sa nachadzájú údaje o cenníkoch a vybavení športovísk. Nasledujúca tabuľka
informácie o športoviskách kde každé z nich má pridelenú informáciu o tom aké vybavenie obsahuje a jeho hodinovej sadzbe.
Posledná tabuľka eviduje údaje rezerváciach. Nájdeme tu údaje o dátume a čase rezervácie, počte ľudoch a zarezervovanom
športovisku.
<br><br>
Každá tabuľka obsahuje všetky základné funkcie práce s nimi, tj. vytvorenie, prezeranie, aktualizovanie a
vymazanie údajov z tabuliek. Všetky tabuľky májú možnosť stránkovania pri vačšom množstve uložených údajov. Na úvodnej
obrazovke môžeme nájsť 4 grafy znázorňujúce niektoré údaje z tabuliek v grafickej podobe.
